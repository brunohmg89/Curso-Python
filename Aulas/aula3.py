# Python já entende que é um numero inteiro dentro dos parenteses
print(1234)

# Aspas simples
print('Bruno Henrique')
print(1, 'Bruno "Henrique"')

# Aspas duplas
print("Bruno Henrique")
print(2, "Bruno 'Henrique'")

# Escape
print("Bruno \"Henrique\"")

# r
print(r"Bruno \"Henrique\"")